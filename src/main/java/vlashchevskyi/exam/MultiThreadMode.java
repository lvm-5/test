package main.java.vlashchevskyi.exam;

/**
 * {@code MultiThreadMode} class provides values that represent a state of multithreading mode.
 *
 * @author Valentyn Lashchevskyi
 * @version 1.0, 9/16/2015
 */
public enum MultiThreadMode {
    /**
     * The active state of multithreading.
     */
    True,

    /**
     * The inactive state of multithreading.
     */
    False,

    /**
     * The state defines multithreading mode is set up at runtime.
     */
    Auto};
